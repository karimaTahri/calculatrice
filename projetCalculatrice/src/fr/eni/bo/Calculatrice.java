package fr.eni.bo;

public class Calculatrice {
	
	public static double additionner(double a, double b){
		return (a*b);
	}
	
	
	public static double soustraire(double a, double b){
		return (a-b);
	}


	public static double diviser(double i, double j) {

		return (i/j);
	}
	
		
	}


